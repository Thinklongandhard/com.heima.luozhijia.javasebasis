public class Paihe {
    private String num;
    private String color;

    public Paihe() {
    }

    public Paihe(String num, String color) {
        this.num = num;
        this.color = color;
    }

    public String  getNum() {
        return num;
    }

    public void setNum(String  num) {
        this.num = num;
    }

    public String getColor() {
        return color;
    }

    public void setColor(String color) {
        this.color = color;
    }
}
